package fr.imie.formation.DTO;

public class DroitProfilDTO {
	
	private int numProfil;
	private int Fonction;
	private int NatureDroit;
	
	public int getNumProfil() {
		return numProfil;
	}
	public void setNumProfil(int numProfil) {
		this.numProfil = numProfil;
	}
	public int getFonction() {
		return Fonction;
	}
	public void setFonction(int fonction) {
		Fonction = fonction;
	}
	public int getNatureDroit() {
		return NatureDroit;
	}
	public void setNatureDroit(int natureDroit) {
		NatureDroit = natureDroit;
	}

}
